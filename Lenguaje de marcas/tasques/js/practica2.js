"use strict";

function calcularNumero(num1, num2) {
    console.log(`La suma de ${num1} y ${num2} es: ${num1 + num2} `);
    console.log(`La resta de ${num1} y ${num2} es: ${num1 - num2} `);
    console.log(`La muktiplicación de ${num1} y ${num2} es: ${num1 * num2} `);
    console.log(`La división de ${num1} y ${num2} es: ${num1 /num2} `);
}

// pruebas con distintos inputs
calcularNumero(10, 5);
calcularNumero(20, 4);
calcularNumero(15, 3);  